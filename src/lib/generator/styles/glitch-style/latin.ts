export const baseLatinCharacters = Object.keys(LatinCharactersMap);

function getRandomLatinVariants(char: string): string {
	if (!baseLatinCharacters.includes(char)) return char;

	const latinVariant = LatinCharactersMap[char as BaseCharacters];
	const result = getRandomValueFromArray(latinVariant);
	return result;
}

export function latinizeText(text: string): string {
	for (const char of text) {
		text = text.replaceAll(char, getRandomLatinVariants(char));
	}
	return text;
}
